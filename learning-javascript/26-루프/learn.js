//[짝수 출력] for 문을 사용해 1부터 100까지의 숫자 중, 짝수만 출력
for (let i = 2; i <= 100; i += 2) {
  console.log(i);
}
console.log("================");

// [카운트다운] while 문을 사용해 숫자 10부터 1까지 역순으로 출력
{
  let i = 10;
  while (i > 0) {
    console.log(i);
    i--;
  }
}
console.log("================");

// [과일바구니] for … of 문을 사용해 다음 배열의 과일 출력
const fruits = ["사과", "바나나", "귤", "복숭아"];
for (let item of fruits) {
  console.log(item);
}
console.log("================");

// [비밀번호 찾기] do … while 문을 사용해 입력된 비밀번호('3001') 매칭 검토 후 결과 출력
let i = 0,
  attempt;

const input = "3001",
  attempts = ["0124", "8291", "8259", "3001", "9073"];

do {
  attempt = attempts[i++];
  console.log(attempt);
} while (input !== attempt);

console.log("================");

// [성적표 출력] for … in 문을 사용해 객체의 key, value 출력
const student = {
  name: "선호",
  grade: "A",
  age: 22,
};

for (let key in student) {
  let value = student[key];
  console.log(key + " : " + value);
}
console.log("================");

// 6. [까다로운 반복문] `1`부터 `20`까지 반복
//    1. `5`부터 `10`까지는 건너띄고 나머지 출력
//    2. `17`이 되면 반복 종료
{
  for (let i = 1; i <= 20; i++) {
    if (5 <= i && i <= 10) {
      console.log("continue " + i);
      continue;
    }
    if (i === 17) break;
    console.log(i);
  }
}
console.log("================");
